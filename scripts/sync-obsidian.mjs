const writes = []; // collected for --check / --dry

function writeFile(absPath, content) {
  if (FLAG_CHECK) {
    const existing = existsSync(absPath) ? readFileSync(absPath, 'utf-8') : null;
    if (existing !== content) driftLog.push(relative(VAULT, absPath));
    return;
  }
  if (FLAG_DRY) {
    writes.push(relative(VAULT, absPath));
    return;
  }
  mkdirSync(dirname(absPath), { recursive: true });
  writeFileSync(absPath, content);
}

function isOurRegenOutput(entryPath) {
  // Files: check the file's frontmatter for the marker.
  // Folders: check the folder's _agent.md for the marker.
  if (!statSync(entryPath).isDirectory()) {
    if (!entryPath.endsWith('.md')) return false;
    return readFileSync(entryPath, 'utf-8').includes('generated_by: scripts/sync-obsidian.mjs');
  }
  const marker = join(entryPath, '_agent.md');
  if (!existsSync(marker)) return false;
  return readFileSync(marker, 'utf-8').includes('generated_by: scripts/sync-obsidian.mjs');
}

function archiveCurrentLive() {
  // Find the next available archive number. v1 already exists.
  let n = 2;
  while (existsSync(join(VAULT, `_Archive v${n}`))) n++;
  const archiveDir = join(VAULT, `_Archive v${n}`);

  const movables = [];
  for (const entry of readdirSync(VAULT)) {
    if (entry.startsWith('_Archive')) continue;
    if (entry === 'Legal Research') continue; // user's reference notes
    if (entry === 'Background') continue; // already organized
    if (entry.startsWith('.')) continue;

    const p = join(VAULT, entry);
    if (PERSONAL_NOTES_TO_ARCHIVE.includes(entry)) {
      // Skip if this is our own regenerated Index.md (subsequent sync runs).
      if (!isOurRegenOutput(p)) movables.push(entry);
      continue;
    }
    if (NEVER_OVERWRITE_REGENERATED_DIRS.includes(entry)) {
      // Only archive a regen folder if it's NOT our previous regen output
      // — i.e., it's hand-authored legacy content. Our own output gets
      // wiped in clearAutoFolders() instead.
      if (!isOurRegenOutput(p)) movables.push(entry);
    }
  }

  if (movables.length === 0) {
    console.log('  (no live content to archive)');
    return null;
  }

  console.log(`  archiving ${movables.length} entr${movables.length === 1 ? 'y' : 'ies'} → _Archive v${n}/`);

  if (FLAG_DRY || FLAG_CHECK) return archiveDir;

  mkdirSync(archiveDir, { recursive: true });
  // Add an _agent.md to the archive
  writeFileSync(join(archiveDir, '_agent.md'), `---
title: _agent — Archive v${n}
type: agent-note
created: ${new Date().toISOString()}
tags:
  - archive
  - meta
---

# _agent — what this folder is

The pre-${new Date().toISOString().slice(0, 10)} live state of the vault, archived
when \`scripts/sync-obsidian.mjs\` regenerated the vault from the
shipped codebase.

## Rules

- Do not edit. History is history.
- Do not move files back into the live tree.
- Do not read these files when answering questions about the
  current state of the site — use the live regenerated content
  in the parent folder.

## Why it's here

The user's "never overwrite" rule. Every time the vault is rebuilt
from the codebase, the prior state is preserved in a numbered archive.
`);

  for (const entry of movables) {
    const src = join(VAULT, entry);
    const dest = join(archiveDir, entry);
    renameSync(src, dest);
  }
  return archiveDir;
}

function clearAutoFolders() {
  // Wipe the auto-generated folders before regen so deleted entries
  // (e.g., a clause that was removed) don't linger.
  if (FLAG_CHECK || FLAG_DRY) return;
  for (const dir of NEVER_OVERWRITE_REGENERATED_DIRS) {
    const p = join(VAULT, dir);
    if (existsSync(p)) rmSync(p, { recursive: true, force: true });
  }
}

function listCitations() {
  const byCategory = {};
  for (const cat of readdirSync(CITATIONS_SRC)) {
    const dir = join(CITATIONS_SRC, cat);
    if (!statSync(dir).isDirectory()) continue;
    byCategory[cat] = readdirSync(dir)
      .filter(f => f.endsWith('.md'))
      .map(f => join(dir, f))
      .sort();
  }
  return byCategory;
}

