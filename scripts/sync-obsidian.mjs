function main() {
  if (FLAG_CHECK) {
    console.log(`Checking vault drift at ${VAULT} …`);
  } else if (FLAG_DRY) {
    console.log(`Dry-run — showing what would be written to ${VAULT}`);
  } else {
    console.log(`Syncing vault at ${VAULT} …`);
    archiveCurrentLive();
    clearAutoFolders();
  }

  // Top-level files
  writeFile(join(VAULT, '_agent.md'), genVaultAgent());
  writeFile(join(VAULT, 'README.md'), genReadme());
  writeFile(join(VAULT, 'Index.md'), genIndex());

  // Clauses
  writeFile(join(VAULT, 'Clauses', '_agent.md'), genClausesAgent());
  writeFile(join(VAULT, 'Clauses', 'Clauses Index.md'), genClausesIndex(data.clauses));
  for (const c of data.clauses) {
    writeFile(join(VAULT, 'Clauses', `${c.n} - ${c.title}.md`), genClause(c));
  }

  // Email Templates
  writeFile(join(VAULT, 'Email Templates', '_agent.md'), genTemplatesAgent());
  writeFile(join(VAULT, 'Email Templates', 'Templates Index.md'), genTemplatesIndex(data.templates));
  for (const t of data.templates) {
    writeFile(join(VAULT, 'Email Templates', `${t.code} - ${t.title}.md`), genTemplate(t));
  }

  // Citations
  const cit = listCitations();
  writeFile(join(VAULT, 'Citations', '_agent.md'), genCitationsAgent());
  writeFile(join(VAULT, 'Citations', 'Citations Index.md'), genCitationsIndex(cit));
  for (const [cat, files] of Object.entries(cit)) {
    for (const f of files) {
      const dest = join(VAULT, 'Citations', cat, basename(f));
      writeFile(dest, genCitationFile(f, cat));
    }
  }

  // Site Copy
  writeFile(join(VAULT, 'Site Copy', '_agent.md'), genSiteCopyAgent());
  writeFile(join(VAULT, 'Site Copy', 'Homepage.md'), genSiteCopyHomepage(data));
  for (const slug of ['onus-of-proof', 'aedilitian-remedies', 'limits']) {
    const c = genSiteCopyDeepDive(slug);
    if (c) {
      const niceName = slug.split('-').map(w => w[0].toUpperCase() + w.slice(1)).join(' ');
      writeFile(join(VAULT, 'Site Copy', `${niceName}.md`), c);
    }
  }

  // Sections (homepage subsections)
  writeFile(join(VAULT, 'Sections', '_agent.md'), genSectionsAgent());
  writeFile(join(VAULT, 'Sections', 'Quick Hits.md'), genQuickHits(data));
  writeFile(join(VAULT, 'Sections', 'Law Reference.md'), genLawReference(data));
  writeFile(join(VAULT, 'Sections', 'FAQ.md'), genFaq(data));
  writeFile(join(VAULT, 'Sections', 'Escalation Ladder.md'), genEscalation(data));
  writeFile(join(VAULT, 'Sections', 'Manufacturer Warranties.md'), genWarranty(data));
  writeFile(join(VAULT, 'Sections', 'Takealot Policies Summary.md'), genPolicySummary(data));
  writeFile(join(VAULT, 'Sections', 'Playbook.md'), genPlaybook(data));

  // Background — only seed if missing (these are personal notes the
  // user maintains by hand after the first run).
  const bgDir = join(VAULT, 'Background');
  const seedBackground = (rel, gen) => {
    const p = join(bgDir, rel);
    if (FLAG_CHECK) return; // background is user-maintained, not drift-checked
    if (existsSync(p) && !FLAG_DRY) return;
    writeFile(p, gen());
  };
  seedBackground('_agent.md', genBackgroundAgent);
  seedBackground('Project Story.md', genProjectStory);

  // Pull personal notes from the most recent archive on first sync.
  // After first sync these files exist in Background/ and are
  // user-maintained — the seedBackground guard skips them.
  if (!FLAG_CHECK && !FLAG_DRY) {
    const archives = readdirSync(VAULT)
      .filter(n => n.startsWith('_Archive v'))
      .sort((a, b) => parseInt(b.split('v')[1]) - parseInt(a.split('v')[1]));
    if (archives.length > 0) {
      const latest = join(VAULT, archives[0]);
      for (const noteName of ['Original Dispute.md', 'CGSO Complaint 64QR12K7.md']) {
        const src = join(latest, noteName);
        const dest = join(bgDir, noteName);
        if (existsSync(src) && !existsSync(dest)) {
          writeFileSync(dest, readFileSync(src));
        }
      }
    }
  }

  // Report
  if (FLAG_CHECK) {
    if (driftLog.length === 0) {
      console.log('✓ vault is in sync with the codebase.');
      return;
    }
    console.error('✗ vault drift detected — these files differ from what the codebase would generate:');
    for (const f of driftLog.slice(0, 30)) console.error(`  ${f}`);
    if (driftLog.length > 30) console.error(`  … and ${driftLog.length - 30} more`);
    console.error('');
    console.error('Fix: run `npm run sync:obsidian` to regenerate the vault.');
    process.exit(1);
  }
  if (FLAG_DRY) {
    console.log(`would write ${writes.length} files`);
    for (const w of writes.slice(0, 50)) console.log(`  ${w}`);
    if (writes.length > 50) console.log(`  … and ${writes.length - 50} more`);
    return;
  }
  console.log('✓ vault synced.');
}

main();
