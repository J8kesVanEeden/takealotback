function citationSlug(filename) {
  return filename.replace(/\.md$/, '').toLowerCase();
}

function genCitationsIndex(byCategory) {
  const blocks = Object.entries(byCategory).map(([cat, files]) => {
    const rows = files.map(f => {
      const slug = citationSlug(basename(f));
      const fm = readFrontmatter(f);
      const title = fm.title || basename(f, '.md');
      return `- [[${cat}/${basename(f, '.md')}\\|${title}]] — ${fm.metaDescription || fm.short || ''}`;
    }).join('\n');
    return `## ${cat[0].toUpperCase() + cat.slice(1)}\n\n${rows}`;
  }).join('\n\n');

  return `---
title: Citations Index
generated_by: scripts/sync-obsidian.mjs
tags:
  - moc
  - citations
---

# Citations Index

Mirrors https://takealotback.com/citations. Each entry has its own
file under the matching subfolder.

${blocks}

[[../Index|← Back to vault index]]
`;
}

function readFrontmatter(file) {
  const content = readFileSync(file, 'utf-8');
  const m = content.match(/^---\n([\s\S]*?)\n---/);
  if (!m) return {};
  const fm = {};
  for (const line of m[1].split('\n')) {
    const kv = line.match(/^(\w+):\s*(.*)$/);
    if (kv) fm[kv[1]] = kv[2].replace(/^['"]|['"]$/g, '');
  }
  return fm;
}

function genCitationFile(srcFile, category) {
  const content = readFileSync(srcFile, 'utf-8');
  const fm = readFrontmatter(srcFile);
  const slug = basename(srcFile, '.md').toLowerCase();
  const liveUrl = `https://takealotback.com/citations/${category}/${slug}`;
  // Re-emit the file with a header pointing to the live URL.
  return `---
generated_by: scripts/sync-obsidian.mjs
source_file: src/content/citations/${category}/${basename(srcFile)}
live_url: ${liveUrl}
tags:
  - citation
  - ${category}
---

> **Live page:** [${liveUrl}](${liveUrl})
> **Source:** \`src/content/citations/${category}/${basename(srcFile)}\`

${content}
`;
}

